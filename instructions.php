            <div>
                <h2 class="text-2xl md:text-3xl font-bold text-green-400 border-b-2 border-green-400 pb-2">3. The Five Phases</h2>
                <p class="mt-4 text-gray-300">
                    The game board is divided into five distinct phases, like lines on a subway map. Each phase is a self-contained competition with a key deliverable.
                </p>
                <ul class="mt-4 list-disc list-inside text-gray-300 space-y-2">
                    <li><b>Briefing:</b> Deliverable is the Client Brief.</li>
                    <li><b>Strategy:</b> Deliverable is the Creative Brief.</li>
                    <li><b>Creative Ideation:</b> Deliverable is The Big Idea.</li>
                    <li><b>Creative Development:</b> Deliverable is the Campaign Creative.</li>
                    <li><b>Production:</b> Deliverable is the Final Campaign Assets.</li>
                </ul>
