                <h2 class="text-2xl md:text-3xl font-bold text-green-400 border-b-2 border-green-400 pb-2">2. Objective & Winning</h2>
                <p class="mt-4 text-gray-300">
                    The game is played over five separate phases. The player who wins a phase by being the first to reach the end of the board is awarded a prestigious NZ advertising award. The ultimate objective is to win the most awards. The player with the most awards at the end of the five phases wins the grand prize: a coveted Cannes Lion.
                </p>
