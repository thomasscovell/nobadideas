            <div>
                <h2 class="text-2xl md:text-3xl font-bold text-green-400 border-b-2 border-green-400 pb-2">4. How to Play</h2>
                <p class="mt-4 text-gray-300">
                    The goal is to be the first to complete each campaign phase and win an award token. The player with the most awards after five phases wins!
                </p>
                <ol class="mt-4 list-decimal list-inside text-gray-300 space-y-2">
                    <li><b>Setup:</b> All players place their chosen Role token on the starting space of the current phase.</li>
                    <li><b>On Your Turn:</b> Roll the dice and move forward that many spaces.</li>
                    <li>If you land on a <b>named deliverable or activity</b>, you are safe.</li>
                    <li>If you land on a <b>blank space</b>, you must draw a "Campaign Twist" card from the app and follow the instructions.</li>
                    <li>Once per phase, you may choose to draw a "Media Boost" card from the pile and play it immediately.</li>
                    <li><b>Winning a Phase:</b> The first player to land on or pass the final space of a board wins the phase and collects the Award Card for that phase. The phase then immediately ends, and all players move their tokens to the start of the next phase.</li>
                </ol>
            </div>

            <div>
                <h2 class="text-2xl md:text-3xl font-bold text-green-400 border-b-2 border-green-400 pb-2">5. Core Gameplay Loop</h2>
                <ul class="mt-4 list-disc list-inside text-gray-300 space-y-3">
                    <li><b>Create The Brief (Mad Libs style):</b> At the very start of the game, all players participate in a "Mad Libs" exercise to create a unique starting brief for context.</li>
                    <li><b>Start a Phase:</b> All players place their tokens on the starting space of the current board.</li>
                    <li><b>On Your Turn:</b>
                        <ul class="list-disc list-inside ml-6 mt-2 space-y-2">
                            <li><b>Draw a Media Boost (Optional):</b> Once per phase, on your turn, you may choose to draw one "Media Boost" card and play it immediately.</li>
                            <li><b>Roll and Move:</b> Roll the dice and move your token along the path.</li>
                            <li><b>Land on a Space:</b> If it's a named space, you are safe. If it's a blank space, you must draw a "Campaign Twist" card.</li>
                        </ul>
                    </li>
                    <li><b>Win the Phase & Collect an Award:</b> The first player to land on or pass the final space of the current board immediately wins that phase and collects the corresponding award card.</li>
                    <li><b>Start the Next Phase:</b> As soon as one player wins, the current phase is over. All players, regardless of their position, immediately move their tokens to the starting space of the next board. This process repeats until all five phases are complete.</li>
                </ul>
