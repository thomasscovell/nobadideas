                <h2 class="text-2xl md:text-3xl font-bold text-green-400 border-b-2 border-green-400 pb-2">1. Game Concept</h2>
                <p class="mt-4 text-gray-300">
                    "No, Bad Ideas!" is a collaborative and competitive board game for 2-4 players. It simulates the chaotic, challenging, and ultimately rewarding process of taking a client brief from initial concept to a full campaign launch. At the start of the game, players choose an Agency Role to represent them on their journey, coordinating with various internal and external partners to navigate the project timeline. The game is designed to teach media professionals about the creative process, fostering empathy and better collaboration.
                </p>
